- (void)drawCornerWithCorners:(UIRectCorner)corners withCornerRadii:(CGSize)cornerRadii{
    UIBezierPath*path = [UIBezierPath bezierPathWithRoundedRect:self.bounds byRoundingCorners:corners cornerRadii:cornerRadii];
    CAShapeLayer*la = [[CAShapeLayer alloc] init];
    la.frame = self.bounds;
    la.path= path.CGPath;
    self.layer.mask = la;
}
